# Create a list using []
a = [10,11,12,13,14,15,16]

# print the list using print() function
print(a)     # return --> [10, 11, 12, 13, 14, 15, 16]

# Access list index using a[index]
print(a[0])  # return --> 10
print(a[4])  # return --> 14

# change list value
a[0] = 50
print(a)     # return --> [50, 11, 12, 13, 14, 15, 16]

# we can create list different type
b = [10, 'Abhay', True, 6.5]
print(b)