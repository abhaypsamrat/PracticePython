# Arithmetic operator
a=5
b=6
print("The value of 5+6 is :",a+b)
print("The value of 5-6 is :",a-b)
print("The value of 5*6 is :",a*b)
print("The value of 5/6 is :",a/b)

# Assignment operator
a=30
a += 2
print(a)


# Comparison operator(It return boolean)
x=(3>5)
y=(5!=4)
print(x)
print(y)

# Logical operator
bool1=True
bool2=False
print("Value of bool1 and bool2 is :", (bool1 and bool2))
print("Value of bool1 or bool2 is :", (bool1 or bool2))
print("Value of not bool2 is :", (not bool2))